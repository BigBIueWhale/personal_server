#!/usr/bin/env bash
# scripts/00_install_codex_cli_undo.sh — defensively undo the per-user Codex
# CLI configuration written by 00_install_codex_cli.sh.
#
# WHAT IT UNDOES
# --------------
#   (d) Removes the three managed profile files ONLY IF each exists with the
#       exact byte content written by the installer.
#   (c) Removes the exact marker-delimited root profile-selection block from
#       ~/.codex/config.toml ONLY IF present and byte-identical. If the root
#       profile was user-owned (same value but no installer block), this script
#       leaves it alone.
#   (b) Removes the exact ~/.bashrc PATH snippet written by the installer, if
#       present. If ~/.local/bin was already on PATH before install, there is
#       nothing to remove.
#   (a) Does NOT uninstall the Codex binary/package and never touches auth,
#       sessions, logs, history, plugins, MCP config, themes, or other Codex
#       state under ~/.codex.
#
# DESIGN — TWO-PHASE, ALL-OR-NOTHING
# ----------------------------------
# Phase 1 verifies every exact byte sequence and collects every problem before
# any write. If there is a problem, the script exits non-zero and touches
# nothing. Phase 2 performs all removals with tempfile + atomic rename where a
# rewrite is needed.
#
# Usage:
#   bash scripts/00_install_codex_cli_undo.sh

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
. "$SCRIPT_DIR/lib/common.sh"

require_non_root
require_ubuntu_noble
require_command python3

TARGET_USER="$(id -un)"
TARGET_HOME="$HOME"
[ "$TARGET_HOME" = "/home/$TARGET_USER" ] \
    || die "expected HOME=/home/$TARGET_USER, got '$TARGET_HOME' — refusing to write into a non-standard home"

if [ -n "${CODEX_HOME:-}" ] && [ "$CODEX_HOME" != "$TARGET_HOME/.codex" ]; then
    die "CODEX_HOME is set to '$CODEX_HOME'. This undo script manages the default $TARGET_HOME/.codex only; unset CODEX_HOME and re-run."
fi

BASHRC="$TARGET_HOME/.bashrc"
CODEX_HOME_DIR="$TARGET_HOME/.codex"
CONFIG="$CODEX_HOME_DIR/config.toml"
SAFE_PROFILE="$CODEX_HOME_DIR/personal-server-safe.config.toml"
WORKSPACE_PROFILE="$CODEX_HOME_DIR/personal-server-workspace.config.toml"
DANGER_PROFILE="$CODEX_HOME_DIR/personal-server-container-danger.config.toml"

[ -f "$BASHRC" ] || die "$BASHRC does not exist — nothing to undo for PATH step"

section "verify Codex managed state before undo"

export U_BASHRC="$BASHRC"
export U_CONFIG="$CONFIG"
export U_SAFE_PROFILE="$SAFE_PROFILE"
export U_WORKSPACE_PROFILE="$WORKSPACE_PROFILE"
export U_DANGER_PROFILE="$DANGER_PROFILE"

python3 - <<'PYEOF'
