STEP_B_APPEND = (
    "\n"
    "# Added by 00_install_codex_cli.sh — codex lives in ~/.local/bin\n"
    "export PATH=\"$HOME/.local/bin:$PATH\"\n"
)

problems: list[str] = []


def read(path: pathlib.Path) -> str:
    return path.read_text(encoding="utf-8")


def atomic_write(path: pathlib.Path, text: str) -> None:
    fd, tmp = tempfile.mkstemp(prefix=path.name + ".new.", dir=str(path.parent))
    try:
        with os.fdopen(fd, "w", encoding="utf-8") as f:
            f.write(text)
        os.replace(tmp, path)
    except BaseException:
        try:
            os.unlink(tmp)
        except FileNotFoundError:
            pass
        raise

# Phase 1: verify config block if present. Missing config/block is okay: install
# may have left a user-owned matching profile selection alone.
config_text = ""
if CONFIG.exists():
    if not CONFIG.is_file():
        problems.append(f"{CONFIG}: exists but is not a regular file")
    else:
        config_text = read(CONFIG)
        try:
            tomllib.loads(config_text or "")
        except tomllib.TOMLDecodeError as e:
            problems.append(f"{CONFIG}: not valid TOML: {e}")
        n_begin = config_text.count(ROOT_BEGIN)
        n_end = config_text.count(ROOT_END)
        if n_begin != n_end:
            problems.append(f"{CONFIG}: managed profile-selection markers are unbalanced ({n_begin} begin, {n_end} end)")
        elif n_begin > 1:
            problems.append(f"{CONFIG}: more than one managed profile-selection block exists")
        elif n_begin == 1 and ROOT_BLOCK not in config_text:
            problems.append(f"{CONFIG}: managed profile-selection block is present but differs from installer expectation")

for path, expected in [
    (SAFE_PROFILE, SAFE_CONTENT),
    (WORKSPACE_PROFILE, WORKSPACE_CONTENT),
    (DANGER_PROFILE, DANGER_CONTENT),
]:
    if not path.exists():
        problems.append(f"{path}: managed profile is missing")
    elif not path.is_file():
        problems.append(f"{path}: exists but is not a regular file")
    elif read(path) != expected:
        problems.append(f"{path}: content differs from installer-managed bytes")

bashrc_text = read(BASHRC)
n_b = bashrc_text.count(STEP_B_APPEND)
if n_b > 1:
    problems.append(f"{BASHRC}: Codex PATH snippet appears {n_b} times; expected 0 or 1")

if problems:
    print("[fatal] Phase 1 verification failed — refusing to undo anything.", file=sys.stderr)
    print("        No file has been touched. Problems:", file=sys.stderr)
    for p in problems:
        print(f"  - {p}", file=sys.stderr)
    sys.exit(2)

print("[info] Phase 1: managed Codex state verified.")

# Phase 2: remove exact state.
for path in [SAFE_PROFILE, WORKSPACE_PROFILE, DANGER_PROFILE]:
    path.unlink()
    print(f"[info] deleted {path}")

if CONFIG.exists() and ROOT_BLOCK in config_text:
    new_config = config_text.replace(ROOT_BLOCK + "\n", "", 1)
    if new_config == config_text:
        new_config = config_text.replace(ROOT_BLOCK, "", 1)
    tomllib.loads(new_config or "")
    atomic_write(CONFIG, new_config)
    print(f"[info] removed managed profile-selection block from {CONFIG}")
else:
    print(f"[info] no installer-owned profile-selection block in {CONFIG}; leaving config.toml alone")

if n_b == 1:
    new_bashrc = bashrc_text.replace(STEP_B_APPEND, "", 1)
    atomic_write(BASHRC, new_bashrc)
    print(f"[info] removed Codex PATH snippet from {BASHRC}")
else:
    print(f"[info] no installer-owned Codex PATH snippet in {BASHRC}; leaving PATH config alone")

print("[info] Codex managed configuration undo complete")
